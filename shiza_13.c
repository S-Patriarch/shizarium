/*
   Это отличный пример того, как форма записи кода может создавать
   иллюзию математической нотации, хотя на самом деле работает по
   правилам языка программирования.

   В языке программирования С сочетание символов x --> 0 не является
   единым оператором и не означает "стремится к нулю", как в
   математике.

   В С пробелы между операторами не важны для синтаксического
   разбора (кроме некоторых исключений). Компилятор использует
   алгоритм "maximal munch" (берет максимально возможную
   последовательность символов как токен).

   Но --> не является валидным оператором в С, поэтому он
   разбивается на известные токены:
        -- (декремент)
        >  (больше)

   Вывод:
   1. x --> 0 - это просто x-- > 0, популярный "пасхальный" способ
   записи уменьшения переменной.
   2. Следует писать явно: 
        while (x-- > 0) или 
        while (x > 0) { x--; ... }
*/

#include <stdlib.h>
#include <stdio.h>

int
main(void) 
{
        int     x;

        x = 10;
        while (x --> 0)
                printf("%d ", x);

        putchar((int)'\n');
        exit(0);
}

